class Product:
    def __init__(self, id, product, brand, number_of_product, expire_date, price, total_price):
        self.id = id
        self.product = product
        self.brand = brand
        self.number_of_product = number_of_product
        self.expire_date = expire_date
        self.price = price
        self.total_price = total_price
